def greater_or_equal(lst, x):
    l = 0
    r = len(lst) - 1
    if lst[r] < x:
        return len(lst)
    while (l + 1 < r):
        m = int((l + r) / 2)
        if lst[m] > x:
            r = m
        elif lst[m] < x:
            l = m
        else:
            return m
    if lst[l] >= x:
        return l
    return r


def smaller_or_equal(lst, x):
    l = 0
    r = len(lst) - 1
    if lst[l] > x:
        return -1
    while (l + 1 < r):
        m = int((l + r) / 2)
        if lst[m] > x:
            r = m
        elif lst[m] < x:
            l = m
        else:
            return m
    if lst[r] <= x:
        return r
    return l


def make_set(x):
    rank[x] = 0
    parent[x] = x


def find_set(x):
    if parent[x] != x:
        parent[x] = find_set(parent[x])
    return parent[x]


def union(x, y):
    link(find_set(x), find_set(y))


def link(x, y):
    if x != y:
        if rank[x] > rank[y]:
            parent[y] = x
        else:
            parent[x] = parent[y]
            if rank[x] == rank[y]:
                rank[y] += 1


def isPrime(n):
    if n == 2:
        return True
    if n % 2 == 0 or n <= 1:
        return False
    sqr = int(math.sqrt(n)) + 1
    for divisor in range(3, sqr, 2):
        if n % divisor == 0:
            return False
    return True


